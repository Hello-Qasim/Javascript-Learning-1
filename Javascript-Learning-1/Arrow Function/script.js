// Arrow Functions !

// In arrow we don not write the keyword "function" and we also don not give name to the function.we stored the value to the variable.

const checkExpenses = (dailyExpenses) => 2000 - dailyExpenses;

const getExpenses = checkExpenses(150);
console.log(getExpenses);

// This is how to write the arrow function in which we don not need to mention "function" keyword or parenthesis () for passing the parameter or the curly bracket {} for code to return.
